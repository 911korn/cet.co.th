// ============ Language switching ============
function applyLang(lang) {
  if (!I18N[lang]) lang = 'en';
  const dict = I18N[lang];

  document.documentElement.setAttribute('lang', lang);

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  try { localStorage.setItem('cet.lang', lang); } catch (_) { /* ignore */ }

  // Update meta description per lang
  const desc = document.querySelector('meta[name="description"]');
  if (desc) {
    desc.setAttribute('content', lang === 'th'
      ? 'บริษัท ซีอีที (ไทยแลนด์) จำกัด — บริษัทพัฒนาซอฟต์แวร์ แอปพลิเคชันมือถือ และ AI ในกรุงเทพฯ ผู้สร้าง easysub.io'
      : 'CET (Thailand) Company Limited is a Bangkok-based software house specializing in software, mobile app, and AI development. Makers of easysub.io.');
  }
}

function detectInitialLang() {
  try {
    const saved = localStorage.getItem('cet.lang');
    if (saved) return saved;
  } catch (_) { /* ignore */ }
  const browser = (navigator.language || 'en').toLowerCase();
  return browser.startsWith('th') ? 'th' : 'en';
}

document.addEventListener('DOMContentLoaded', () => {
  applyLang(detectInitialLang());

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => applyLang(btn.dataset.lang));
  });

  const yearEl = document.getElementById('year');
  if (yearEl) yearEl.textContent = new Date().getFullYear();
});
